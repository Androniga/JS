arr = [-3, 8, 7, 6, 5, -4, 3, 2, 1];
var i, j, x;
for (i = 0; i <= 9; i++) {
  for (j = i + 1; j <= 9; j++) {
    if (arr[i] > arr[j]) {
      x = arr[i];
      arr[i] = arr[j];
      arr[j] = x;
    }
  }
}
